<h3>GUEST WELLBEING IS OUR UTMOST PRIORITY</h3>

Our chain of properties are designed with a generosity of space, which is expansive in comparison to the number of guests present at any given point. This added advantage allows a hugely reduced chance of an outbreak.

Workplace health and safety support:
<ul>
 <li>Our resort locations are secluded and away from madding crowds. These private locations offer reduced pollution levels, minimal people interaction, and zero decibel getaways.</li>
<li>Our accommodations are designed to be independent and they are far and in between, this ensures not just privacy but appropriate distancing by default.</li>
<li>All entrants including guests and associates either on foot or in a vehicle are temperature screened to ensure safety inside the resort premises.</li>
<li>Contents carried into the resorts by guests and associates are screened and disinfected at the security before being allowed to enter.</li>
<li>Protective masks and other safety gears are mandatory to worn by the staff.</li>
<li>Guidelines also includes prominently placing hand sanitizers throughout hotel public areas and entrances, as well as introducing social distancing (at least 1 meter) signage that will provide guidance for guests in public spaces with the screening of body temperature of guests & the employees.</li>
<li>Our guests can check-in without any human intervention. We will provide you the means to send all relevant information and documents online, which you can share even before arriving at the resort.</li>
<li>During check-in, guests are escorted directly to their designated room post arrival at the resort.</li>
<li>The bath in each accommodation has adequate amount of soaps and we do not allow refills as a policy, only fresh soaps and toiletries are used for every new guest.</li>
<li>For metallic surfaces like door handles, security locks, keys etc 70 % alcohol will be used to wipe surface, where the use of bleach is not suitable.</li>
<li>The food is freshly prepared to be served immediately from kitchen to table ensuring maximum hygiene - we have done away with buffets for the moment.</li>
<li>You can choose where to dine, we provide table service at our restaurants and practice adequate social distancing. If you are more comfortable being served in the confines of your accommodation please let us know, we deliver.</li>
<li>Our accommodations have independent AC units, hence the utilization is left to guests’ discretion. They can be sure that the air is not shared unlike in centralized AC systems. These units are sanitized after every checkout.</li>
<li>Guests booking any of our transport services such as taxis can be confident that all vehicles will be sanitized before being made available. Chauffeur accompanying you will maintain safe distancing protocols and will be wearing safety gear through the entire journey.</li>
<li>Please feel free to request for masks and sanitizers if you seek to use them at our properties. Please note that guests are not obligated to wear masks while staying with us.</li>
